          <div class="col-xl-10" >
            <ul class="faq-list" id="list">
              <li>
                <div data-bs-toggle="collapse" class="collapsed question" href="#faq1">What is intentional transitional ministry? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq1" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    Transitional ministry is pastoral ministry. Transitional pastors shepherd congregations
through the uncertain period between the departure of one pastor and its search for a
new long-term pastor to lead the church in the future.
                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq2" class="collapsed question">How does intentional transitional pastoral ministry differ from interim
ministry? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq2" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    The two terms are somewhat synonymous. We prefer to speak about transitional
pastoral ministry because interim pastors have traditionally provided pulpit supply and
general pastoral care for congregants. Transitional pastors do more. They make an
intentional effort to prepare the church for the future by working with lay leaders to
clarify the congregation’s mission, vision, and values, resolve conflict, train leadership,
evaluate ministries and guide the church through the pastoral search process
                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq3" class="collapsed question">What does a transitional pastor do? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq3" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    Transitional pastors serve as the congregation’s senior pastor. Specific responsibilities
are spelled out in the Memorandum of Understanding (MOU), but the primary purpose of transitional ministry is to get the
congregation ready for its next long-term pastor. Transitional ministry focuses on
several tasks: 1) restoring hope, 2) resolving conflict, 3) rebuilding mission, 4) restoring
vision, 5) recruiting and training lay leaders, and 6) resetting governance.
                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq4" class="collapsed question">Does intentional transitional ministry require special training?
 <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq4" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    Training builds upon your previous ministry experience. It provides a framework for
accomplishing the particular goals and objectives of transitional ministry. ATMN requires
training for certification as a member of the Network
                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq5" class="collapsed question">Where can I get the training, I need to be certified by ATMN? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq5" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    Training is available through a variety of sources. ATMN encourages pastors to consider
training offered by Interim Pastor Ministry and VitalChurch Ministries. We will soon be
able to offer Alliance pastors our own training program on the C&MA’s learning
platform. In addition, certification does require training in Alliance peacemaking. More
information about Alliance peacemaking can be found on the website: Peacemaking
(cmalliance.org)
                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq6" class="collapsed question">What are the benefits of ATMN certification? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq6" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    There are advantages to becoming a part of the Alliance Transitional Ministries
Network. ATMN certification verifies your aptitude and qualifications for transitional 
ministry. It assures district superintendents that you have completed training for
transitional ministry. In addition, the ATMN team is developing resources and coaching
opportunities to assist transitional pastors
                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq7" class="collapsed question">What is the purpose of certification? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq7" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    ATMN certification is intended to verify your previous ministry experience, the skills
needed for transitional ministry, and training for transitional ministry and peacemaking.
                  </p>
                </div>
              </li>

               <li>
                <div data-bs-toggle="collapse" href="#faq8" class="collapsed question">How do I become certified for transitional ministry in the Alliance? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq8" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    After securing the necessary training, applicants must complete the Request for
Certification and be approved by the Office of Church Advance.

                  </p>
                </div>
              </li>

               <li>
                <div data-bs-toggle="collapse" href="#faq9" class="collapsed question">Once I am certified, how can I be placed in a ministry position? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq9" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    ATMN does not act as a placement agency. Alliance transitional pastors are placed by
the district superintendent overseeing the church in transition. Districts receive periodic
updates indicating who is certified by ATMN and their status (assigned or unassigned).
Superintendents are encouraged to contact certified pastors directly. Of course, you
may also make the district aware of your availability directly.
                  </p>
                </div>
              </li>

               <li>
                <div data-bs-toggle="collapse" href="#faq10" class="collapsed question">What kinds of assignments are available in my area? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq10" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    One ministry organization estimates that about 10% of the congregations seeking
pastoral candidates need to engage a transitional pastor before they call the next
pastor. Contact your district superintendent to discover if there are churches in your
region searching for a transitional pastor.
                  </p>
                </div>
              </li>

               <li>
                <div data-bs-toggle="collapse" href="#faq11" class="collapsed question">I have a retired official worker license. Can I still serve as a transitional
pastor? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq11" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    While not all transitional pastors are retired official workers, many are. Your Retired
Official Worker’s license allows you to serve in interim ministry. Many other transitional
pastors continue to hold an active official worker’s license. They are licensed by the
district in which they reside as a transitional pastor, even if they are serving a church in
another district.
                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq12" class="collapsed question">How do district superintendents know that I am available for transitional
ministry? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq12" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    ATMN sends an updated list of certified ATMN pastors to districts periodically. When the
ATMN portal is complete, district superintendents will have direct access to the current
list of available pastors.
                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq13" class="collapsed question">What is the salary of a transitional pastor? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq13" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    In most circumstances, transitional pastors receive the same salary as the church
provided its previous pastor. In some circumstances, when the local church resources
are limited, districts have supplemented the church’s provision with an additional
amount. Salaries are negotiated at the beginning of the transitional ministry. Host
congregations are also expected to provide furnished housing. 
                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq14" class="collapsed question">How long are transitional ministry assignments? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq14" class="collapse" data-bs-parent=".faq-list">
                  <p>
                    Transitional assignments vary in length, from about 6 months to as many as 18 months.
Occasionally they may extend to 24 months, but that is not standard. 

                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq15" class="collapsed question">If I am certified as an ATMN pastor, am I required to accept an assignment? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq15" class="collapse" data-bs-parent=".faq-list">
                  <p>
                   District superintendents will contact certified pastors to determine interest in accepting
an assignment. Whether you do so or not is entirely up to you and your sense of God’s
leading.
                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq16" class="collapsed question">Does transitional ministry require me to be away from my home? <i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq16" class="collapse" data-bs-parent=".faq-list">
                  <p>
                   Although some transitional assignments may be close to home, that is not always the
case. Applicants for certification indicate the distance they are willing to travel from
their residence on the Request for Certification. The transitional pastor decides what
distance from their home they wish to serve. Some pastors move to the location, some
commute daily and others rotate their time between locations. Those arrangements are
negotiated in the MOU
                  </p>
                </div>
              </li>

              <li>
                <div data-bs-toggle="collapse" href="#faq17" class="collapsed question">If I accept a call to serve a church some distance from my place of residence,
will I be required to move?<i class="bi bi-chevron-down icon-show"></i><i class="bi bi-chevron-up icon-close"></i></div>
                <div id="faq17" class="collapse" data-bs-parent=".faq-list">
                  <p>
                   There are several models for transitional ministry. A few pastors do decide to move to
the church’s location until the church calls a new pastor. When the place of ministry is
close to one’s residence, pastors often commute. Those who are serving a long distance
from their place of residence, commonly spend three weeks (including Sundays) on site
and one week working remotely from their place of residence. These arrangements
should be stipulated in the MOU negotiated with the district and the church.
                  </p>
                </div>
              </li>
            </ul>
